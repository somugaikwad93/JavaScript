//concat
var fullName = "Somanath " + "Gaikwad";
console.log(fullName)

//concat using variables
var firstName = "Somanath"
var lastName = "Gaikwad"
console.log(firstName + " " + lastName)

//concat using plus-equal 
var fullName = "Somanath "
fullName += "Gaikwad"
console.log(fullName)

//length of the string
var lengthOfString = fullName.length;
console.log(lengthOfString)

//first charater of the string
var firstCharacter = fullName[0];
console.log(firstCharacter);

//last charater of the string
var lastCharacter = fullName[fullName.length-1];
console.log(lastCharacter);

